day = int(input("Enter a number of day(number write between 0 to 6): "))

if (day == 0):
    print("Monday")
elif (day == 1):
    print("Tuesday")
elif (day == 2):
    print("Wednesday")
elif (day == 3):
    print("Thursday")
elif (day == 4):
    print("Friday")
elif (day == 5):
    print("Saturday")
else:
    print("Sunday")
